async function seedSingleType(uid, data, locale) {
  console.log(`[legal] Seeding ${uid} (locale: ${locale}) …`);
  try {
    const existing = await strapi.entityService.findMany(uid, {
      locale,
    });

    const payload = {
      ...data,
      locale,
      publishedAt: new Date().toISOString(),
    };

    if (existing && existing.id) {
      await strapi.entityService.update(uid, existing.id, { data: payload });
      console.log(`[legal] Updated ${uid} (id: ${existing.id})`);
    } else {
      await strapi.entityService.create(uid, { data: payload });
      console.log(`[legal] Created ${uid}`);
    }
  } catch (err) {
    console.error(`[legal] Failed to seed ${uid}:`, err.message);
    throw err;
  }
}

